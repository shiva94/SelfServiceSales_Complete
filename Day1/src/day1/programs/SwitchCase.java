package day1.programs;

public class SwitchCase {

	public static void main(String[] args) {
		String x="ten";
		switch(x){
		case "one":
			System.out.println("No match");
			break;
		case "two":
			System.out.println("No match");
			break;
		case "ten":
			System.out.println("Match");
			break;
		
		}
		

	}

}
